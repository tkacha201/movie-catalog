/**
 * Temporary script — fetches real TMDB data and writes it to mockData.ts
 * Run: node scripts/fetch-mock-data.mjs
 * Then delete this script.
 */

const TOKEN = process.env.EXPO_PUBLIC_TMDB_API_TOKEN || process.argv[2];

if (!TOKEN) {
  console.error('Usage: EXPO_PUBLIC_TMDB_API_TOKEN=xxx node scripts/fetch-mock-data.mjs');
  console.error('   or: node scripts/fetch-mock-data.mjs <token>');
  process.exit(1);
}

const BASE = 'https://api.themoviedb.org/3';
const headers = { Authorization: `Bearer ${TOKEN}`, 'Content-Type': 'application/json' };

async function fetchPages(endpoint, params, pages) {
  const results = [];
  for (let page = 1; page <= pages; page++) {
    const url = new URL(`${BASE}${endpoint}`);
    url.searchParams.set('page', String(page));
    for (const [k, v] of Object.entries(params)) url.searchParams.set(k, v);
    const res = await fetch(url.toString(), { headers });
    if (!res.ok) throw new Error(`TMDB ${res.status}: ${await res.text()}`);
    const data = await res.json();
    results.push(...data.results);
    console.log(`  ${endpoint} page ${page}/${pages} — ${data.results.length} items`);
  }
  return results;
}

function formatMovie(m) {
  return `  {
    id: ${m.id},
    title: ${JSON.stringify(m.title)},
    poster_path: ${m.poster_path ? JSON.stringify(m.poster_path) : 'null'},
    backdrop_path: ${m.backdrop_path ? JSON.stringify(m.backdrop_path) : 'null'},
    release_date: ${JSON.stringify(m.release_date || '')},
    overview: ${JSON.stringify(m.overview || '')},
    vote_average: ${m.vote_average ?? 0},
    genre_ids: [${(m.genre_ids || []).join(', ')}],
  }`;
}

async function main() {
  console.log('Fetching popular movies (5 pages = ~100)...');
  const popular = await fetchPages('/discover/movie', { sort_by: 'popularity.desc' }, 5);

  console.log('Fetching top rated movies (3 pages = ~60)...');
  const topRated = await fetchPages('/movie/top_rated', {}, 3);

  console.log('Fetching upcoming movies (2 pages = ~40)...');
  const today = new Date().toISOString().slice(0, 10);
  const endOfYear = `${new Date().getFullYear()}-12-31`;
  const upcoming = await fetchPages('/discover/movie', {
    sort_by: 'release_date.asc',
    'primary_release_date.gte': today,
    'primary_release_date.lte': endOfYear,
    with_release_type: '2|3',
  }, 2);

  // Deduplicate by id, prefer popular list
  const seenIds = new Set();
  const allBrowse = [];
  for (const m of [...popular, ...topRated]) {
    if (!seenIds.has(m.id)) {
      seenIds.add(m.id);
      allBrowse.push(m);
    }
  }

  // Remove any upcoming from browse
  const upcomingIds = new Set(upcoming.map((m) => m.id));
  const browse = allBrowse.filter((m) => !upcomingIds.has(m.id));

  console.log(`\nTotal: ${browse.length} browse + ${upcoming.length} upcoming`);

  // Also fetch details for a subset (first 20) so we have runtime/genres for detail pages
  console.log('Fetching details for first 20 movies...');
  const detailsMap = {};
  const detailIds = browse.slice(0, 20).map((m) => m.id);
  for (const id of detailIds) {
    const res = await fetch(`${BASE}/movie/${id}`, { headers });
    if (res.ok) {
      const d = await res.json();
      detailsMap[id] = { runtime: d.runtime, genres: d.genres, tagline: d.tagline || '' };
    }
  }

  // Generate TypeScript file
  const ts = `/**
 * Pre-fetched TMDB movie data used as fallback when no API token is configured.
 * Generated by scripts/fetch-mock-data.mjs on ${new Date().toISOString().slice(0, 10)}.
 * Contains ${browse.length} browse movies and ${upcoming.length} upcoming movies.
 */
import type { TMDBMovie, TMDBMovieDetails } from './movieService';

export const MOCK_MOVIES: TMDBMovie[] = [
${browse.map(formatMovie).join(',\n')}
];

export const MOCK_UPCOMING: TMDBMovie[] = [
${upcoming.map(formatMovie).join(',\n')}
];

const MOCK_DETAILS: Record<number, { runtime: number | null; genres: { id: number; name: string }[]; tagline: string }> = {
${Object.entries(detailsMap).map(([id, d]) => `  ${id}: { runtime: ${d.runtime ?? 'null'}, genres: ${JSON.stringify(d.genres)}, tagline: ${JSON.stringify(d.tagline)} }`).join(',\n')}
};

const GENRE_MAP: Record<number, string> = {
  28: 'Action', 12: 'Adventure', 16: 'Animation', 35: 'Comedy', 80: 'Crime',
  99: 'Documentary', 18: 'Drama', 10751: 'Family', 14: 'Fantasy', 36: 'History',
  27: 'Horror', 10402: 'Music', 9648: 'Mystery', 10749: 'Romance', 878: 'Sci-Fi',
  10770: 'TV Movie', 53: 'Thriller', 10752: 'War', 37: 'Western',
};

export function getMockMovieDetails(id: number): TMDBMovieDetails | null {
  const movie = [...MOCK_MOVIES, ...MOCK_UPCOMING].find((m) => m.id === id);
  if (!movie) return null;

  const cached = MOCK_DETAILS[id];
  if (cached) {
    return { ...movie, ...cached };
  }

  return {
    ...movie,
    runtime: 120,
    tagline: '',
    genres: (movie.genre_ids ?? []).map((gid) => ({
      id: gid,
      name: GENRE_MAP[gid] ?? 'Unknown',
    })),
  };
}
`;

  const { writeFileSync } = await import('fs');
  writeFileSync('src/services/mockData.ts', ts, 'utf-8');
  console.log('\n✅ Written to src/services/mockData.ts');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
